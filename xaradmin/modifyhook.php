/**
 * modify an entry for a module item - hook for ('item','modify','GUI')
 *
 * @param $args['objectid'] ID of the object
 * @param $args['extrainfo'] extra information
 * @return string hook output in HTML
 * @throws BAD_PARAM, NO_PERMISSION, DATABASE_ERROR
 */
function changelog_admin_modifyhook($args)
{
    extract($args);

    if (!isset($extrainfo)) {
        $msg = xarML(
            'Invalid #(1) for #(2) function #(3)() in module #(4)',
            'extrainfo',
            'admin',
            'modifyhook',
            'changelog'
        );
        xarErrorSet(
            XAR_USER_EXCEPTION,
            'BAD_PARAM',
            new SystemException($msg)
        );
        return $msg;
    }

    if (!isset($objectid) || !is_numeric($objectid)) {
        $msg = xarML(
            'Invalid #(1) for #(2) function #(3)() in module #(4)',
            'object ID',
            'admin',
            'modifyhook',
            'changelog'
        );
        xarErrorSet(
            XAR_USER_EXCEPTION,
            'BAD_PARAM',
            new SystemException($msg)
        );
        return $msg;
    }

    // When called via hooks, the module name may be empty, so we get it from
    // the current module
    if (empty($extrainfo['module'])) {
        $modname = xarModGetName();
    } else {
        $modname = $extrainfo['module'];
    }

    $modid = xarMod::getRegId($modname);
    if (empty($modid)) {
        $msg = xarML(
            'Invalid #(1) for #(2) function #(3)() in module #(4)',
            'module name',
            'admin',
            'modifyhook',
            'changelog'
        );
        xarErrorSet(
            XAR_USER_EXCEPTION,
            'BAD_PARAM',
            new SystemException($msg)
        );
        return $msg;
    }

    if (!empty($extrainfo['itemtype']) && is_numeric($extrainfo['itemtype'])) {
        $itemtype = $extrainfo['itemtype'];
    } else {
        $itemtype = 0;
    }

    if (!empty($extrainfo['itemid']) && is_numeric($extrainfo['itemid'])) {
        $itemid = $extrainfo['itemid'];
    } else {
        $itemid = $objectid;
    }

    if (!empty($extrainfo['changelog_remark'])) {
        $remark = $extrainfo['changelog_remark'];
    } else {
        xarVarFetch('changelog_remark', 'str:1:', $remark, null, XARVAR_NOT_REQUIRED);
        if (empty($remark)) {
            $remark = '';
        }
    }

    if (xarSecurityCheck('ReadChangeLog', 0, 'Item', "$modid:$itemtype:$itemid")) {
        $link = xarModURL(
            'changelog',
            'admin',
            'showlog',
            ['modid' => $modid,
                  'itemtype' => $itemtype,
                  'itemid' => $itemid]
        );
    } else {
        $link = '';
    }

    return xarTplModule(
        'changelog',
        'admin',
        'modifyhook',
        ['remark' => $remark,
              'link' => $link]
    );
}
